/**
 * Object used to keep KEY-VALUE
 * @param <K> Type of KEY
 * @param <V> Type of VALUE
 */
public class Entry<K, V> {
    private K key;
    private V value;

    public Entry(K key, V value) {
        this.key = key;
        this.value = value;
    }

    public Entry() {
        this.key = null;
        this.value = null;
    }

    public K getKey() {
        return key;
    }

    public void setKey(K key) {
        this.key = key;
    }

    public V getValue() {
        return value;
    }

    public void setValue(V value) {
        this.value = value;
    }
}
